    public static function convertDate($dateToConvert)
    {
        if (null === $dateToConvert) {
            return null;
        }

        if ($dateToConvert instanceof \DateTime) {
            return $dateToConvert;
        }

        if (0 == strlen(trim($dateToConvert))) {
            return null;
        }

        // Gestion des date Excel stoqués en nombre de jours depuis le 01/01/1900
        if (is_numeric($dateToConvert)) {
            $timestamp = ($dateToConvert - 25569) * 86400;

            $result = new \DateTime();
            $result->setTimestamp($timestamp);

            return $result;
        }

        if (10 != strlen($dateToConvert)) {
            return false;
        }

        $jour = substr($dateToConvert, 0, 2);
        $mois = substr($dateToConvert, 3, 2);
        $annee = substr($dateToConvert, 6, 4);

        if (!is_numeric($jour) || !is_numeric($mois) || !is_numeric($annee)) {
            return false;
        }

        if (!checkdate($mois, $jour, $annee)) {
            return false;
        }

        $result = \DateTime::createFromFormat('d/m/Y', $dateToConvert);

        return $result;
    }

